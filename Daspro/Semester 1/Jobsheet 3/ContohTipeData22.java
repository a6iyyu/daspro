public class ContohTipeData22 {
    public static void main(String[] args) {
        char golongan_darah = 'A';
        byte jarak = (byte) 130;
        short jumlah_penduduk_dalam_dusun = 1025;
        float suhu = 60.5F;
        double berat = 0.5467812345;
        long saldo = 150000000;
        int angka_desimal = 0x10;

        System.out.println("Golongan darah\t\t\t: " + (byte) golongan_darah);
        System.out.println("Jarak\t\t\t\t: " + jarak);
        System.out.println("Jumlah penduduk dalam satu dusun: " + jumlah_penduduk_dalam_dusun);
        System.out.println("Suhu saat ini\t\t\t: " + suhu);
        System.out.println("Berat suatu benda\t\t: " + (float) berat);
        System.out.println("Saldo saya saat ini\t\t: " + saldo);
        System.out.println("Angka desimal yang dipilih\t: " + angka_desimal);
    }
}
